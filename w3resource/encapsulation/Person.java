public class Person {
    private String name;
    private int age;
    private String country;

    public String getName(){
        return name;
    }
    public int getage(){
        return age;
    }
    public String getCountry(){
        return country;
    }
    public void setName(String newName){
        this.name = newName;
    }
    public void setage(int newage){
        this.age = newage;
    }
    public void setCountry(String newCountry){
        this.country = newCountry;
    }

}

